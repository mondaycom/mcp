const SECOND_IN_MILLISECOND = 1000;
export const NANOSECONDS_PER_MILLISECOND = 1_000_000;

export enum TIME_IN_SECONDS {
  YEAR = 365 * 24 * 60 * 60,
  MONTH31Days = 31 * 24 * 60 * 60,
  MONTH30Days = 30 * 24 * 60 * 60,
  MONTH29Days = 29 * 24 * 60 * 60,
  MONTH28Days = 28 * 24 * 60 * 60,
  WEEK = 7 * 24 * 60 * 60,
  DAY = 24 * 60 * 60,
  HOUR = 60 * 60,
  MINUTE = 60,
}

export enum TIME_IN_MILLISECONDS {
  YEAR = 365 * 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  MONTH31Days = 31 * 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  MONTH30Days = 30 * 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  MONTH29Days = 29 * 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  MONTH28Days = 28 * 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  DAY = 24 * 60 * 60 * SECOND_IN_MILLISECOND,
  HOUR = 60 * 60 * SECOND_IN_MILLISECOND,
  MINUTE = 60 * SECOND_IN_MILLISECOND,
  SECOND = SECOND_IN_MILLISECOND,
}
